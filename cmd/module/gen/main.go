// Generator for the module registry. Invoke via `go generate ./cmd/module/...`
// or `make generate` from the repo root. DO NOT run directly from other directories.
//
// Discovery rule: a directory under modules/ is included if and only if it
// contains an fx/migration.go file.
package main

import (
	"errors"
	"fmt"
	"log"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
	"unicode"
)

const (
	goModulePath = "github.com/dinhtp/lee-goo"
	outFile      = "registry.go"
)

func main() {
	// go generate sets CWD to the directory of the file containing the directive
	// (cmd/module/), so ../../modules reaches the modules root.
	cwd, err := os.Getwd()
	if err != nil {
		log.Fatal(err)
	}

	modulesDir := filepath.Join(cwd, "..", "..", "modules")
	if _, err := os.Stat(modulesDir); err != nil {
		log.Fatalf("modules dir not found at %s — run via `make generate` or `go generate ./cmd/module/...`", modulesDir)
	}

	names, err := discoverModules(modulesDir)
	if err != nil {
		log.Fatal(err)
	}
	if len(names) == 0 {
		log.Fatal("no modules discovered — check that modules have fx/migration.go")
	}

	outPath := filepath.Join(modulesDir, outFile)
	if err := writeRegistry(names, outPath); err != nil {
		log.Fatal(err)
	}

	if err := exec.Command("go", "fmt", outPath).Run(); err != nil {
		log.Fatal("go fmt:", err)
	}

	fmt.Printf("Generated %s with %d module(s): %s\n", outFile, len(names), strings.Join(names, ", "))
}

// discoverModules scans modulesDir for subdirectories that contain fx/migration.go.
// Returns sorted module names.
func discoverModules(modulesDir string) ([]string, error) {
	entries, err := os.ReadDir(modulesDir)
	if err != nil {
		return nil, fmt.Errorf("read modules dir: %w", err)
	}

	var names []string
	for _, e := range entries {
		if !e.IsDir() {
			continue
		}
		marker := filepath.Join(modulesDir, e.Name(), "fx", "migration.go")
		if _, err := os.Stat(marker); errors.Is(err, os.ErrNotExist) {
			continue
		} else if err != nil {
			return nil, fmt.Errorf("stat %s: %w", marker, err)
		}
		names = append(names, e.Name())
	}

	sort.Strings(names)
	return names, nil
}

// moduleAlias converts a module name to a camelCase Go import alias.
// "authorization" → "authorizationModule", "my-billing" → "myBillingModule"
func moduleAlias(name string) string {
	parts := strings.FieldsFunc(name, func(r rune) bool {
		return !unicode.IsLetter(r) && !unicode.IsDigit(r)
	})
	var b strings.Builder
	for i, p := range parts {
		if i == 0 {
			b.WriteString(strings.ToLower(p))
		} else if len(p) > 0 {
			b.WriteRune(unicode.ToUpper(rune(p[0])))
			b.WriteString(strings.ToLower(p[1:]))
		}
	}
	b.WriteString("Module")
	return b.String()
}

// writeRegistry writes modules/registry.go as package modules with an exported Registry() function.
func writeRegistry(names []string, outPath string) error {
	// Detect alias collisions before writing.
	seen := make(map[string]string, len(names))
	for _, n := range names {
		alias := moduleAlias(n)
		if conflict, ok := seen[alias]; ok {
			log.Fatalf("alias collision: modules %q and %q both produce alias %q — rename one module", conflict, n, alias)
		}
		seen[alias] = n
	}

	f, err := os.Create(outPath)
	if err != nil {
		return fmt.Errorf("create %s: %w", outPath, err)
	}
	defer f.Close()

	w := func(format string, a ...any) {
		fmt.Fprintf(f, format, a...)
	}

	w("// Code generated by cmd/module/gen. DO NOT EDIT.\n")
	w("package modules\n\n")
	w("import (\n")
	for _, n := range names {
		w("\t%s %q\n", moduleAlias(n), goModulePath+"/modules/"+n+"/fx")
	}
	w("\n")
	w("\t%q\n", goModulePath+"/system/migrator")
	w(")\n\n")
	w("// Registry returns all registered module migration sources, keyed by module name.\n")
	w("// Run `make generate` to refresh after adding a module.\n")
	w("func Registry() map[string]migrator.Source {\n")
	w("\treturn map[string]migrator.Source{\n")
	for _, n := range names {
		w("\t\t%q: %s.Source(),\n", n, moduleAlias(n))
	}
	w("\t}\n")
	w("}\n")

	return nil
}
